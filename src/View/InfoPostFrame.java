    private void bnt_danglaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bnt_danglaiMouseClicked
        Calendar now = Calendar.getInstance();
        this.post.setDop_post(now);
        PostController pc = new PostController();
        int input = JOptionPane.showConfirmDialog(null, "Bạn muốn đăng lại bài đã đăng?","CẢNH BÁO!", JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);
        if(input == JOptionPane.YES_OPTION)
        {
            pc.updatePost(this.post);
            JOptionPane.showMessageDialog(null, "Đăng lại thành công! Kiểm tra trong Tin Của Bạn!");
            mf.loadListPostChoThue();
            mf.loadTinCuaBan();
            this.setVisible(false);
        }
            
    }//GEN-LAST:event_bnt_danglaiMouseClicked

