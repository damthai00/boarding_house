    public void loadPost(Post post){
        ImageIcon icon = new ImageIcon(new ImageIcon(post.getImg_cover()).getImage().getScaledInstance(350, 350, Image.SCALE_SMOOTH));        
        this.img.setIcon(icon);
        this.img.setText(null);
        
        ImageIcon icon_cover = new ImageIcon(new ImageIcon(post.getImg_cover()).getImage().getScaledInstance(50, 50, Image.SCALE_SMOOTH));        
        this.img_cover.setIcon(icon_cover);
        this.img_cover.setText(null);
        
        ImageIcon icon1 = new ImageIcon(new ImageIcon(post.getImg1()).getImage().getScaledInstance(50, 50, Image.SCALE_SMOOTH));        
        this.img1.setIcon(icon1);
        this.img1.setText(null);
        ImageIcon icon2 = new ImageIcon(new ImageIcon(post.getImg2()).getImage().getScaledInstance(50, 50, Image.SCALE_SMOOTH));        
        this.img2.setIcon(icon2);
        this.img2.setText(null);
        ImageIcon icon3 = new ImageIcon(new ImageIcon(post.getImg3()).getImage().getScaledInstance(50, 50, Image.SCALE_SMOOTH));        
        this.img3.setIcon(icon3);
        this.img3.setText(null);
        ImageIcon icon4 = new ImageIcon(new ImageIcon(post.getImg4()).getImage().getScaledInstance(50, 50, Image.SCALE_SMOOTH));        
        this.img4.setIcon(icon4);
        this.img4.setText(null);
        
        this.title.setText(post.getTitle());
        
        String priceString = ConvertMoneyVN.getInstance().LongToMoney(post.getPrice());
        this.price.setText(priceString + "/Tháng");
        
        this.area.setText(post.getArea()+" m vuông");
        this.adress.setText(post.getAdress_post());
        this.detail.setText(post.getDetail());
        
        int day = post.getDop_post().get(Calendar.DAY_OF_MONTH);
        int Month = post.getDop_post().get(Calendar.MONTH);
        int year = post.getDop_post().get(Calendar.YEAR);
        String date = String.valueOf(day) + "/"+ String.valueOf(Month +1)+"/"+String.valueOf(year);
        this.dop_post.setText(date);
        
        this.contact.setText(post.getContact());
        
    }
    
    public void loadUser(Post post){
        UserController uc = new UserController();
        ArrayList<User> list = uc.getUser();
        User user = new User();
        for(int i=0;i<list.size();i++)
            if(list.get(i).getId_user() == post.getId_user())
            {
                user = list.get(i);
                break;
            }

        ImageIcon icon = new ImageIcon(new ImageIcon(user.getImg()).getImage().getScaledInstance(100, 100, Image.SCALE_SMOOTH));        
        this.img_user.setIcon(icon);
        this.img_user.setText(null);
        
        this.adress_user.setText(user.getAdress_user());
        this.phone.setText(String.valueOf(user.getPhone()));
        this.fullname.setText(user.getFullname());
        this.emai.setText(user.getEmail());
        
        int day = user.getDop_user().get(Calendar.DAY_OF_MONTH);
         int Month = user.getDop_user().get(Calendar.MONTH);
          int year = user.getDop_user().get(Calendar.YEAR);
        String date = String.valueOf(day) + "/"+ String.valueOf(Month +1)+"/"+String.valueOf(year);
        this.dop_user.setText(date);
        
    }
    
    
    
    
    

